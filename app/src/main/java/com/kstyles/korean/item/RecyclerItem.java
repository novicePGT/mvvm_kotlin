package com.kstyles.korean.item;

public class RecyclerItem {

    private String level;
    private String name;

    public RecyclerItem() {}

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
